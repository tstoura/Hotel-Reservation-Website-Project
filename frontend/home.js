document.querySelector('.hero button').addEventListener('click', () => {
    window.location.href = "reservation.html"; // Replace "reservation.html" with the path to your reservation page.
  });
  
  document.querySelector('.nav-auth .login').addEventListener('click', () => {
    window.location.href = "login.html"; // Replace "login.html" with the path to your login page.
  });
  
  document.querySelector('.nav-auth .signup').addEventListener('click', () => {
    window.location.href = "signup.html"; // Replace "signup.html" with the path to your signup page.
  });


